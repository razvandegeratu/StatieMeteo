void bme280_read_regs(uint8_t reg, uint8_t *data, uint8_t len) {
    i2c_start(); i2c_write(BME280_ADDR << 1); i2c_write(reg);
    i2c_start(); i2c_write((BME280_ADDR << 1) | 1);
    for (uint8_t i = 0; i < len; i++) data[i] = i2c_read(i < len - 1);
    i2c_stop();
}

